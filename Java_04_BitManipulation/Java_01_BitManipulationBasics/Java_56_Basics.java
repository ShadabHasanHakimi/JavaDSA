package JavaDSA.Java_04_BitManipulation.Java_01_BitManipulationBasics;

public class Java_56_Basics {
    public static void main(String[] args) {
        System.out.println(5&6);        //Bitwise AND(&) Operator
        System.out.println(5|6);        //Bitwise OR(|) Operator
        System.out.println(5^6);        //Bitwise XOR(^) Operator
        System.out.println(~5);         //Binary one's complement or NOT operator

        System.out.println(5<<2);       //Binary left shift
        System.out.println(6>>2);       //Binary right shift
    }
}
